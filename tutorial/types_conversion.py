# You can convert a type to another type in Python using built-in functions like `int()`, `float()`, and `str()`.
# This code snippet demonstrates how to convert between different data types in Python.
# Convert string to integer
num_str = "123"
num_int = int(num_str)
# Convert integer to float
num_float = float(num_int)
# Convert float to string
num_str_from_float = str(num_float)

# Convert integer to string
num_str_from_int = str(num_int)
# Convert float to integer
num_int_from_float = int(num_float)

# Print the results
print(f"String to Integer: {num_int}")
print(f"Integer to Float: {num_float}")
print(f"Float to String: {num_str_from_float}"), print(f"Integer to String: {num_str_from_int}")
print(f"Float to Integer: {num_int_from_float}")

